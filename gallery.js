document.addEventListener('DOMContentLoaded', () => {
    const cardPb = document.getElementById('card-pb');
    const photoGrid = document.getElementById('photoGrid');

    // Page: gallery.html (Landing)
    if (cardPb) {
        // Use a static cover for each category as requested by the user
        // Chose a landscape photo for both to keep the layout uniform
        const pbCoverSrc = galleryData['preto-e-branco'].find(p => p.orientation === 'landscape').src;
        const corCoverSrc = galleryData['coloridas'].find(p => p.orientation === 'landscape').src;

        document.getElementById('cover-pb').src = pbCoverSrc;
        document.getElementById('cover-cor').src = corCoverSrc;
    }

    // Page: series.html (Photo Viewer)
    if (photoGrid) {
        const params = new URLSearchParams(window.location.search);
        // Default to preto-e-branco if ID is missing from URL
        const seriesId = params.get('id') || 'preto-e-branco';

        // Update the page title text
        const titleEl = document.getElementById('series-title');
        if (titleEl) {
            titleEl.textContent = seriesId === 'coloridas' ? 'Coloridas' : 'Preto e Branco';
        }

        const photos = galleryData[seriesId] || [];

        if (photos.length === 0) {
            photoGrid.innerHTML = '<p class="empty-state">Nenhuma foto disponível nesta série ainda.</p>';
        } else {
            photos.forEach(photo => {
                const card = document.createElement('div');
                const orientationClass = photo.orientation || 'portrait';
                card.className = `photo-card fade-in ${orientationClass}`;

                card.innerHTML = `
                    <div class="photo-img-wrapper">
                        <img src="${photo.src}" alt="${photo.title}">
                    </div>
                    <div class="photo-info" style="display:none;">
                        <h3>${photo.title}</h3>
                    </div>
                `;

                card.addEventListener('click', () => {
                    const params = new URLSearchParams({
                        img: photo.src,
                        title: photo.title,
                        artist: photo.artist,
                        desc: photo.description
                    });
                    window.location.href = `index.html?${params.toString()}`;
                });

                photoGrid.appendChild(card);
            });
        }
    }
});
